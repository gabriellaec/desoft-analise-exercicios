class Retangulo:
    def __init__(self,ponto1,ponto2):
        self.psd=ponto2
        self.pontoinfesq=ponto1
    def calcula_perimetro(self):
        per=abs(2*(psd.x-pontoinfesq.x))+abs(2*(pontoinfesq.y-psd.y))
        return per
    def calcula_area(self):
        area=abs(psd.x-pontoinfesq.x)*abs(pontoinfesq.y-psd.y)
        return area