class Pontos:
    def __init__(self,x,y):
        self.x= x
        self.y=y
class Retangulo(self,P1,P2):
    self.P1x= P1.x
    self.P1y=P1.y
    self.P2x= P2.x
    self.P2y= P2.y
    
    def calcula_perimetro(self):
        ladoX= (P1x-P2x)
        ladoY= (P1y-P2y)
        perimetro= ladoX*2+ ladoY*2
        return perimetro
    def calcula_area(self):
        area=ladoX*ladoY
        return area