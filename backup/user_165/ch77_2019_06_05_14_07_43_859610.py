dicionario_atletas = {"Nome1": 1, "Nome2":2,"Nome3":1.5} 
def calcula_tempo(dicionario_atletas):
    dict = {}
    for atleta in dicionario_atletas:
        dict[0] = dicionario_atletas
        aceleracao = dict[atleta]
        tempo = (200*aceleracao)**0.5
        dict[atleta] = tempo
    return dict