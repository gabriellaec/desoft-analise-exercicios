def encontra_maximo(m):
    maior = 0
    lista = []
    x = 0
    while x <= len(m):
        a = 0
        i = 0
        for n[a] in m[i]:
            lista.append(n[a])
            x += 1
            i += 1
            if n[a]>maior:
                maior = n[a]            
            a += 1       
