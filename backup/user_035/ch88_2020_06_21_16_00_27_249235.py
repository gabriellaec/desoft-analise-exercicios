class Retangulo:
    def __init__(self, p1, p2):
        self.pie = p1
        self.psd = p2
        
    def calcula_perimetro(self):
        a = p1.x - p2.x 
        b = p1.y - p2.y
        return (a+b)*2
    
    def calcula_area(self):
        a = p1.x - p2.x
        b = p1.y - p2.y
        return a*b