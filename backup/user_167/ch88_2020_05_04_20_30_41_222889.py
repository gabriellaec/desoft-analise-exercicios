class Retangulo:
    def __init__ (self,a,b):
        self.a=a
        self.b=b
    def calcula_perimetro(self):
        ladoX=(self.a.x-self.b.x)*2
        ladoY=(self.b.y-self.a.y)*2
        return(ladoX+ladoY)

    def calcula_area(self):
        area=(ladoX*  ladoY)/4
        return area
        
  
        
        
   
        
    