class Retangulo(Pontos):
    def __init__(self, p1, p2, x, y):
        self.p1=(x1, y2)
        self.p2=(x2, y2)
        
        def calcula_perimetro(self):
            perimetro_retangulo=2*(x2-x1)+2*(y2-y1)
            self.perimetro=perimetro_retangulo
            
        def calcula_area(self):
            area_retangulo=(x2-x1)*(y2-y1)
            self.area=area_retangulo
         
        
            
        