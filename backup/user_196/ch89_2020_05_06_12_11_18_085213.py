class Circulo:
    def __init__(self, ponto, raio):
        self.ponto = ponto
        self.raio = raio     
    def contem(self, pontok):
        x = self.ponto
        y = self.ponto
        a = self.pontok
        b = self.pontok
        if (x - a)**2 +(y - b)**2 <= raio**2:
            return True